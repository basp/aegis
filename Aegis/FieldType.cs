namespace Aegis
{
    public enum FieldType
    {
        /// <summary>
        /// Specifies an unknown data type.
        /// </summary>
        None = 0,

        /// <summary>
        /// Specifies a <see cref="String"/> data type.
        /// </summary>
        String = 1,

        /// <summary>
        /// Specifies a <see cref="Double"/> data type.
        /// </summary>
        Double = 2,

        /// <summary>
        /// Specifies a <see cref="Int64"/> data type.
        /// </summary>
        Int64 = 3,

        /// <summary>
        /// Specifies a <see cref="Int32"/> data type.
        /// </summary>
        Int32 = 4,
    }
}
