namespace Aegis
{
    public enum FieldType
    {
        /// <summary>
        /// Specifies an unknown data type.
        /// </summary>
        None = 0,

        /// <summary>
        /// Specifies a <see cref="String"/> data type.
        /// </summary>
        String = 1,

        /// <summary>
        /// Specifies a <see cref="Double"/> data type.
        /// </summary>
        Double = 2,

        /// <summary>
        /// Specifies a <see cref="Long"/> data type.
        /// </summary>
        Long = 3,

        /// <summary>
        /// Specifies a <see cref="Int"/> data type.
        /// </summary>
        Int = 4,
    }
}
